# Inteiros:

type(1) # Retorna Int
type(1 + 2) # A soma dá 3 que é Int

# Float:

type(1.) # Retorna Float
type(1. + 2) # A soma dá 3.0 que é Float

# Booleanos:

#True = 1  ///  False = 0
True + False # Retorna 1 ( 1 + 0 )
True * True # Retorna 1 ( 1 * 1 )
True - True # Retorna 0 ( 1 - 1 )

